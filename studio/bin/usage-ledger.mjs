/** The one true ledger schema. `bin/run.sh` writes this header. */
export const CSV_HEADER =
  'utc,role,model,input_tokens,cache_creation_tokens,cache_read_tokens,output_tokens,cost_usd,duration_ms,num_turns,exit';

const TIER_ORDER = ['steward', 'qa', 'council'];

const num = (x) => {
  const n = Number(x);
  return Number.isFinite(n) ? n : 0;
};

/**
 * Build the ledger row for one session from Claude Code's `--output-format
 * json` envelope on stdout, plus the process exit STATUS.
 *
 * On a clean session the envelope parses and we record real usage/cost. On a
 * timeout or crash stdout is not the JSON envelope; we STILL emit a row with
 * zeroed usage tagged `exit=timeout|crash` so misbehaving sessions stay
 * countable in the ledger instead of vanishing (fableton#78).
 *
 * GNU `timeout` exits 124 when it kills the command (137 if SIGKILL); any
 * other non-zero exit with unparseable output is a crash.
 *
 * @returns {{ result: string, row: string }} `result` is the human-readable
 *   text to echo into the transcript; `row` is the CSV line (no newline).
 */
export function buildUsageRow({ raw, role, model, status = 0, now }) {
  const ts = now ?? new Date().toISOString();
  try {
    const r = JSON.parse(raw);
    const u = r.usage ?? {};
    const row = [
      ts,
      role,
      model,
      u.input_tokens ?? 0,
      u.cache_creation_input_tokens ?? 0,
      u.cache_read_input_tokens ?? 0,
      u.output_tokens ?? 0,
      r.total_cost_usd ?? 0,
      r.duration_ms ?? 0,
      r.num_turns ?? 0,
      r.is_error ? 'error' : 'ok',
    ];
    return { result: String(r.result ?? raw), row: row.join(',') };
  } catch {
    const s = num(status);
    const exit = s === 124 || s === 137 ? 'timeout' : 'crash';
    const row = [ts, role, model, 0, 0, 0, 0, 0, 0, 0, exit];
    return { result: raw, row: row.join(',') };
  }
}

function blankTier(role) {
  return {
    role,
    sessions: 0,
    ok: 0,
    timeout: 0,
    crash: 0,
    error: 0,
    input_tokens: 0,
    cache_creation_tokens: 0,
    cache_read_tokens: 0,
    output_tokens: 0,
    cost_usd: 0,
  };
}

/**
 * Aggregate a raw `usage.csv` into redacted per-tier totals. Redaction = drop
 * per-session rows (timestamps, transcripts); keep only tier sums. This is the
 * shape that can safely land off-box so #42's "tokens per tier" is derivable
 * without SSH to the VPS.
 *
 * @returns {Array<ReturnType<typeof blankTier>>} tiers, canonical order first.
 */
export function summarize(csv) {
  const lines = String(csv ?? '')
    .split(/\r?\n/)
    .filter((l) => l.trim() !== '');
  const rows = lines[0] === CSV_HEADER ? lines.slice(1) : lines;
  const byRole = new Map();
  for (const line of rows) {
    const c = line.split(',');
    const role = c[1];
    if (!role) continue;
    const tier = byRole.get(role) ?? blankTier(role);
    tier.sessions += 1;
    const exit = c[10] || 'ok';
    if (exit in tier && exit !== 'role') tier[exit] = (tier[exit] ?? 0) + 1;
    tier.input_tokens += num(c[3]);
    tier.cache_creation_tokens += num(c[4]);
    tier.cache_read_tokens += num(c[5]);
    tier.output_tokens += num(c[6]);
    tier.cost_usd += num(c[7]);
    byRole.set(role, tier);
  }
  const rank = (role) => {
    const i = TIER_ORDER.indexOf(role);
    return i === -1 ? TIER_ORDER.length : i;
  };
  return [...byRole.values()].sort(
    (a, b) => rank(a.role) - rank(b.role) || a.role.localeCompare(b.role),
  );
}

/** Machine-readable surface: one aggregate row per tier. */
export function renderCsv(summary) {
  const header =
    'role,sessions,ok,timeout,crash,error,input_tokens,cache_creation_tokens,cache_read_tokens,output_tokens,cost_usd';
  const rows = summary.map((t) =>
    [
      t.role,
      t.sessions,
      t.ok,
      t.timeout,
      t.crash,
      t.error,
      t.input_tokens,
      t.cache_creation_tokens,
      t.cache_read_tokens,
      t.output_tokens,
      t.cost_usd.toFixed(4),
    ].join(','),
  );
  return [header, ...rows].join('\n') + '\n';
}

/** Human-readable surface. `generatedAt` is injected for determinism. */
export function renderMarkdown(summary, { generatedAt }) {
  const head = [
    '# Tokens per tier',
    '',
    '<!-- AUTO-GENERATED by studio/bin/ledger-snapshot.mjs — do not edit by hand. -->',
    '',
    'Redacted per-tier totals aggregated from the studio VPS token ledger',
    '(`studio/logs/usage.csv`, gitignored). Published off-box so milestone #42\'s',
    'week-close "tokens per tier" stat is derivable without SSH. Per-session rows',
    '(timestamps, transcripts) are intentionally dropped — tier sums only.',
    '',
    `_Generated: ${generatedAt}_`,
    '',
  ];
  if (summary.length === 0) {
    head.push('No sessions recorded yet.');
    return head.join('\n') + '\n';
  }
  const cols = [
    'Tier',
    'Sessions',
    'ok',
    'timeout',
    'crash',
    'error',
    'Input',
    'Cache create',
    'Cache read',
    'Output',
    'Cost (USD)',
  ];
  const total = summarizeTotals(summary);
  const line = (t, label) =>
    `| ${label} | ${t.sessions} | ${t.ok} | ${t.timeout} | ${t.crash} | ${t.error} | ${t.input_tokens} | ${t.cache_creation_tokens} | ${t.cache_read_tokens} | ${t.output_tokens} | ${t.cost_usd.toFixed(4)} |`;
  const rows = [
    `| ${cols.join(' | ')} |`,
    `| ${cols.map(() => '---').join(' | ')} |`,
    ...summary.map((t) => line(t, t.role)),
    line(total, '**total**'),
  ];
  return [...head, ...rows].join('\n') + '\n';
}

function summarizeTotals(summary) {
  const t = blankTier('total');
  for (const s of summary) {
    for (const k of Object.keys(t)) {
      if (k === 'role') continue;
      t[k] += s[k];
    }
  }
  return t;
}
